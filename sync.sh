#!/bin/bash

# Скрипт для синхронизации проекта с GitHub репозиторием
# Будет выгружать последние изменения из GitHub в текущий проект

# Переходим в директорию проекта
cd "$(dirname "$0")"

# Обновляем репозиторий из GitHub
git fetch origin
git reset --hard origin/main

echo "Синхронизация с GitHub завершена успешно!"